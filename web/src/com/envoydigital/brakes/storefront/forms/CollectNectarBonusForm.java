package com.envoydigital.brakes.storefront.forms;

public class CollectNectarBonusForm {

    private String title;
    private String firstName;
    private String lastName;
    private String email;
    private String confirmEmail;
    private String nectarCardNo;
    private String optInCode;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getConfirmEmail() {
        return confirmEmail;
    }

    public void setConfirmEmail(String confirmEmail) {
        this.confirmEmail = confirmEmail;
    }

    public String getNectarCardNo() {
        return nectarCardNo;
    }

    public void setNectarCardNo(String nectarCardNo) {
        this.nectarCardNo = nectarCardNo;
    }

    public String getOptInCode() {
        return optInCode;
    }

    public void setOptInCode(String optInCode) {
        this.optInCode = optInCode;
    }
}
