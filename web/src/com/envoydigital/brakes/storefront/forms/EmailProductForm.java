package com.envoydigital.brakes.storefront.forms;

/**
 * @author Haridaskpillai
 *
 */
public class EmailProductForm
{

	private String emailProductId;
	private String emailAddress;

	/**
	 * @return the emailProductId
	 */
	public String getEmailProductId()
	{
		return emailProductId;
	}

	/**
	 * @param emailProductId
	 *           the emailProductId to set
	 */
	public void setEmailProductId(final String emailProductId)
	{
		this.emailProductId = emailProductId;
	}

	/**
	 * @return the emailAddress
	 */
	public String getEmailAddress()
	{
		return emailAddress;
	}

	/**
	 * @param emailAddress
	 *           the emailAddress to set
	 */
	public void setEmailAddress(final String emailAddress)
	{
		this.emailAddress = emailAddress;
	}


}
